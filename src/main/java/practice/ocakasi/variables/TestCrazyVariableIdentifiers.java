package practice.ocakasi.variables;

public class TestCrazyVariableIdentifiers {

	public static void main(String[] args) {
		int Integer = 10;
		Object Object = new Object();
		System.out.println("Value of the int variable Integer is " + Integer);
		System.out.println("Value of the Object Object is " + Object);
		System.out.println("Such variable identifier named as existing class name in Java Core API is legal but totally bad practice");
		
	}

}
